# максимальная сила стаи (максимум 7 голов у дракона)
def max_power(n: int) -> int:
    # dp[s] = максимальное произведение при сумме s
    dp = [0] * (n + 1)
    dp[0] = 1  # нейтральный элемент для произведения

    for s in range(1, n + 1):
        best = 0
        for k in range(1, 8):  # 1..7
            if s - k >= 0:
                cand = dp[s - k] * k
                if cand > best:
                    best = cand
        dp[s] = best

    return dp[n]

# Немного тестов
def run_tests():
    test_cases = [
        # (вход, ожидаемый результат)
        (1, 1),
        (2, 2),
        (3, 3),
        (4, 4),
        (5, 6),   # 3+2 => 6
        (6, 9),   # 3+3 => 9
        (7, 12),  # 3+4 => 12
        (8, 18),  # 3+3+2 => 18
        (9, 27),  # 3+3+3 => 27
        (10, 36), # 3+3+4 => 36
        (11, 54), # 3+3+3+2 => 54
        (12, 81), # 3+3+3+3 => 81
        (13, 108),
        (14, 162),
        (15, 243),
        (20, 1458),
        (30, 59049),
        (50, 86093442),
        (99, 5559060566555523),
    ]

    print("Запуск тестов...")
    for n, expected in test_cases:
        result = max_power(n)
        assert result == expected, f"(ERR) Ошибка при n={n}: получено {result}, ожидалось {expected}"
        print(f"(OK) n={n} => {result}")

    print("Все тесты пройдены успешно!")

def main():
    print("Введите число голов N:")
    n = int(input().strip())
    print("Максимальная сила стаи:", max_power(n))

if __name__ == "__main__":
    # сначала запускаем тесты
    run_tests()

    # затем обычный режим работы
    main()